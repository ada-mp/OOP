def palindromo_numero():
  palindromo = 9009
  for i in range(100,1000):
    for j in range(100,1000):
      prod = i*j
      if str(prod) == str(prod)[::-1]:
        if prod > palindromo:
          palindromo = prod
  return palindromo

print(palindromo_numero())